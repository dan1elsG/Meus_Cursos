

def dobro_(valor):
    dobro = valor*2
    return dobro


def metade_(valor):
    med = valor / 2
    return med


def aumentar_(valor, taxa=10):
    aumento = valor + (valor * (taxa/100))
    return aumento


def diminuir(valor, taxa=10):
    diminuindo = valor - (valor * (taxa/100))
    return diminuindo
